    private void entrarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_entrarMouseClicked

        try{
            Boolean valid = cp.validarLogin(usuari.getText(), String.valueOf(contrasenya.getPassword()));
            if(String.valueOf(contrasenya.getPassword()).isEmpty() || usuari.getText().isEmpty()){
                lblErrors.setText("S'han de completar tots els camps.");
            }
            else if(usuari.getText().equals("Introdueix el teu nom d'usuari") || String.valueOf(contrasenya.getPassword()).equals("********")){
                lblErrors.setText("S'han de completar tots els camps.");
            }
            else if(!valid){
                boolean existeix = cp.existeixUsuari(usuari.getText());
                if(!existeix){
                    javax.swing.JOptionPane.showMessageDialog(this, "ERROR \nIntent d'inici de sessiÃ³ incorrecte.\nComprova el teu username.\nNo existeix cap compte amb aquest usuari.\nSi no tens compte registra't!");
                }
                else{
                    javax.swing.JOptionPane.showMessageDialog(this, "ERROR \nIntent d'inici de sessiÃ³ incorrecte.\nLa contrasenya NO Ã©s correcta!");
                }
            }
            else{
                cp.realizarLogin(usuari.getText(), String.valueOf(contrasenya.getPassword()));
                cp.mostrarMenuUsuari();
                this.setVisible(false);
                parentMenu.dispose();
            }
        }
        catch(Exception e){
            javax.swing.JOptionPane.showMessageDialog(this, "No s'ha pogut fer l'inici de sessiÃ³: " + e.getMessage(), "ERROR", JOptionPane.ERROR_MESSAGE);
            
            
            
        }
        

    }//GEN-LAST:event_entrarMouseClicked

